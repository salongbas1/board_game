// - background = รูปพื้นหลังแผนที่ ใส่เป็น base64 data URL เช่น "data:image/png;base64,....."
//                หรือปล่อยเป็น null ไว้ก่อนแล้วให้ DM อัปโหลดทีหลังในเกมก็ได้
//
// DM ยังคงสร้าง/ลบ/สลับแผนที่เพิ่มเติมระหว่างเล่นได้ตามปกติ — ไฟล์นี้กำหนดแค่ชุดเริ่มต้น
