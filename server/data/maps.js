const DEFAULT_MAPS = [
  { id: 1, name: 'แผนที่ 1', background: null },
  // ตัวอย่างเพิ่มแผนที่ที่ 2 ไว้ล่วงหน้า — ลบ // ข้างหน้าออกแล้วใส่ background ได้เลย
  // { id: 2, name: 'ดันเจี้ยนใต้ดิน', background: null },
];

// คืนค่าสำเนาใหม่ทุกครั้งที่เรียก กันไม่ให้ทุกห้อง/ทุกรอบเกมอ้างอิง object เดียวกันโดยไม่ตั้งใจ
function cloneDefaultMaps() {
  return DEFAULT_MAPS.map(m => ({ ...m }));
}

module.exports = { DEFAULT_MAPS, cloneDefaultMaps };
